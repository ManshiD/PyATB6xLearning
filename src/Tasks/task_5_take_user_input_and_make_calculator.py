#Make a calculator that takes user input of 2 numbers and do Calculations and show results in float not in integer.

num_1 = input("Enter a number: ")
num_2 = input("Enter another number: ")

sum = float(num_1) + float(num_2)
print(sum)
multiply = float(num_1) * float(num_2)
print(multiply)
divide = float(num_1) / float(num_2)
print(divide)
quotient = float(num_1) // float(num_2)
print(quotient)
remainder = float(num_1) % float(num_2)
print(remainder)
