# Logic Building
# Step 1
# I/P -> num1, num2 -> data type int
# O/P -> sum, mul, div (what datatype Always ask from interviewer), float
num1 = float(input("Enter a number:   "))
num2 = float(input("Enter another number: "))
print(num1 + num2)
print(num1 * num2)
print(num1 / num2)
print(num1 - num2)

